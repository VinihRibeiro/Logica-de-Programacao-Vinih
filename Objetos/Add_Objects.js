// Adicionando propriedades ao objeto 

// Para adicionar uma propriedade a um objeto existente em JavaScript, você pode simplesmente atribuir um valor a uma nova chave ou a uma chave existente do objeto.

// Exemplo:

const pessoa = {
    nome: "Vinicius",
  };
  
  console.log(pessoa); // Saída: { nome: "Vinicius" }

  pessoa.idade = 19; // Adicionando a propriedade "idade" ao objeto "pessoa"
  
  console.log(pessoa); // Saída: { nome: "Vinicius", idade: 19}
  