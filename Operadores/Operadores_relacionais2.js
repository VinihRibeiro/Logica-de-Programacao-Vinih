// Utilizando valores relacionais em variaveis 

let a = 44
let b = 20

console.log(a > b); //resultado: true
console.log(a < b); //resultado: false
console.log(a >= b); //resultado: true
console.log(a <= b); //resultado: false
console.log(a === b); //resultado: false 
console.log(a !== b); //resultado: true
